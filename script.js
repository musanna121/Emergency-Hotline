// National Emergency Number 
document.getElementById("copy-national").
addEventListener("click", function(e){
    e.preventDefault()
    const copyCount = parseInt(document.getElementById("total-copy").innerText)
    newCopyCount = copyCount + 1
    navigator.clipboard.writeText(999);
    document.getElementById("total-copy").innerText = newCopyCount
})

